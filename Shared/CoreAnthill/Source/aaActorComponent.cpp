/// Установка родителя
///
///
///-------------------------------------------------------------------------
void AActorComponent::setActor(const std::weak_ptr<AActor> &parent)
{
	mParent = parent;
}
///-------------------------------------------------------------------------





 ///------------------------------------------------------------------------
///
///
/// 
/// Возвратим родителя
/// внимание вы не должны ударживать родителя, 
/// и наче он не сожмет удалится корректно
///
///-------------------------------------------------------------------------
std::shared_ptr<AActor> AActorComponent :: actor() const
{
	return mParent.lock();
}