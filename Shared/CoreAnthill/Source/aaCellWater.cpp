///
///
/// Destructor
///
///
///-------------------------------------------------------------------------
ACellWater::~ACellWater()
{


}
///-------------------------------------------------------------------------





 ///------------------------------------------------------------------------
///
///
///
/// установка воды
/// налить воду()
///
///-------------------------------------------------------------------------
void ACellWater :: pourWater()
{
    mWater = true;
    mWaterPressure = 1;
    mHumidity = false;
    mHumidityValue = 1.0f;
}
///-------------------------------------------------------------------------








 ///------------------------------------------------------------------------
///
///
/// переливание воды
/// 
///
///
///-------------------------------------------------------------------------
void ACellWater :: moveWaterTo(ACellWater &dest)
{
    dest.mWater = true;
    dest.mWaterPressure = 1;
    dest.mHumidity = false;
    dest.mHumidityValue = minmaxBound<float>(0.0f, 1.0f, dest.mHumidityValue + mHumidityValue);
    
    mWater = false;
    mWaterPressure = 0;
    mHumidity = false;
    mHumidityValue = 0.0f;
}
///-------------------------------------------------------------------------










 ///------------------------------------------------------------------------
///
///
///
/// проверка вода есть или нет
///
///
///-------------------------------------------------------------------------
bool ACellWater :: isWater() const
{
    return mWater;
}
///-------------------------------------------------------------------------











 ///------------------------------------------------------------------------
///
///
///
/// установка давление воды
///
///
///-------------------------------------------------------------------------
void ACellWater :: makePressure(const int pressure)
{
    mWaterPressure = pressure;
}
///-------------------------------------------------------------------------






 ///------------------------------------------------------------------------
///
///
///
/// возвратим давление воды
///
///
///-------------------------------------------------------------------------
int ACellWater::waterPressure() const
{
   return  mWaterPressure;
}
///-------------------------------------------------------------------------






 ///------------------------------------------------------------------------
///
///
///
/// есть влажность или нет
///
///
///-------------------------------------------------------------------------
bool ACellWater::isHumidity() const
{
    return mHumidity;
}
///-------------------------------------------------------------------------






 ///------------------------------------------------------------------------
///
///
///
/// уровень влажности
///
///
///-------------------------------------------------------------------------
float ACellWater::humidityValue() const
{
    return mHumidityValue;
}