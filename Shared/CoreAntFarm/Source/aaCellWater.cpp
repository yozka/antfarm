 ///------------------------------------------------------------------------
///
///
///
/// Constructor
///
///
///-------------------------------------------------------------------------
ACellWater::ACellWater()
{





}
///-------------------------------------------------------------------------





 ///------------------------------------------------------------------------
///
///
///
/// Destructor
///
///
///-------------------------------------------------------------------------
ACellWater::~ACellWater()
{


}
///-------------------------------------------------------------------------





 ///------------------------------------------------------------------------
///
///
///
/// установка воды
///
///
///-------------------------------------------------------------------------
void ACellWater :: makeWater()
{
    mWater = true;
    mWaterFluid = 1.0f;
    mWaterPressure = 1;
}
///-------------------------------------------------------------------------








 ///------------------------------------------------------------------------
///
///
///
/// 
///
///
///-------------------------------------------------------------------------
void ACellWater :: takeWater()
{
    mWater = false;
    mWaterFluid = 0.0f;
    mWaterPressure = 0;
}
///-------------------------------------------------------------------------










 ///------------------------------------------------------------------------
///
///
///
/// проверка вода есть или нет
///
///
///-------------------------------------------------------------------------
bool ACellWater :: isWater() const
{
    return mWater;
}
///-------------------------------------------------------------------------





 ///------------------------------------------------------------------------
///
///
///
/// проверка вода есть, и она уже закончила движение
///
///
///-------------------------------------------------------------------------
bool ACellWater::isWaterFluid() const
{
    return mWater && mWaterFluid < 0.0f;
}
///-------------------------------------------------------------------------






 ///------------------------------------------------------------------------
///
///
///
/// процесс перетекание воды, если вода перетекла то возвращаем true
///
///
///-------------------------------------------------------------------------
bool ACellWater :: waterFluid(const float volume)
{
    if (!mWater)
    {
        return false;
    }

    if (mWaterFluid >= 0.0f)
    {
        mWaterFluid -= volume;
        return false;
    }
    return true;
}
///-------------------------------------------------------------------------






 ///------------------------------------------------------------------------
///
///
///
/// установка давление воды
///
///
///-------------------------------------------------------------------------
void ACellWater :: makePressure(const int pressure)
{
    mWaterPressure = pressure;
}
///-------------------------------------------------------------------------






 ///------------------------------------------------------------------------
///
///
///
/// возвратим давление воды
///
///
///-------------------------------------------------------------------------
int ACellWater::waterPressure() const
{
   return  mWaterPressure;
}


